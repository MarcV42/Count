package org.example;

public class Calculator {

    static int add(int a, int b) {
        return a + b;
    }

    protected static int subtra(int a, int b) {
        return a - b;
    }

    public static int multipl(int a, int b) {
        return a * b;
    }

    public static int division(int a, int b) {
        return a / b;
    }
}
